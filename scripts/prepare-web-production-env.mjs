import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const projectId = 'orbit-ledger-f41c2';
const webAppId = '1:26507257397:web:0fd74ca52a0e2ac969737c';
const outputPath = resolve('apps/web/.env.production.local');
const recaptchaSiteKey =
  process.env.NEXT_PUBLIC_ORBIT_LEDGER_RECAPTCHA_V3_SITE_KEY?.trim() ||
  process.env.ORBIT_LEDGER_RECAPTCHA_V3_SITE_KEY?.trim();

if (!recaptchaSiteKey) {
  console.error('Production App Check reCAPTCHA v3 site key is required.');
  console.error('Set ORBIT_LEDGER_RECAPTCHA_V3_SITE_KEY locally, then rerun this command.');
  process.exit(1);
}

const raw = execFileSync(
  'npx',
  [
    '-y',
    'firebase-tools@13.35.1',
    'apps:sdkconfig',
    'WEB',
    webAppId,
    '--project',
    projectId,
    '--json',
  ],
  { encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] }
);

const response = JSON.parse(raw);
const config = response.result?.sdkConfig ?? response.result;
const requiredConfigKeys = [
  'apiKey',
  'authDomain',
  'projectId',
  'storageBucket',
  'messagingSenderId',
  'appId',
  'measurementId',
];
const missing = requiredConfigKeys.filter((key) => !String(config?.[key] ?? '').trim());

if (missing.length) {
  console.error('Firebase web app config is missing required values.');
  for (const key of missing) {
    console.error(`- ${key}`);
  }
  process.exit(1);
}

if (config.projectId !== projectId) {
  console.error('Firebase web app config does not match the Orbit Ledger production project.');
  process.exit(1);
}

const env = [
  '# Generated by scripts/prepare-web-production-env.mjs',
  '# Local only. Do not commit this file.',
  'NEXT_PUBLIC_ORBIT_LEDGER_ENV=production',
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_API_KEY=${config.apiKey}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_AUTH_DOMAIN=${config.authDomain}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_PROJECT_ID=${config.projectId}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_STORAGE_BUCKET=${config.storageBucket}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_MESSAGING_SENDER_ID=${config.messagingSenderId}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_APP_ID=${config.appId}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_FIREBASE_MEASUREMENT_ID=${config.measurementId}`,
  `NEXT_PUBLIC_ORBIT_LEDGER_RECAPTCHA_V3_SITE_KEY=${recaptchaSiteKey}`,
  '',
].join('\n');

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, env, { mode: 0o600 });

console.log(`Production web env written to ${outputPath}.`);
console.log('Values were not printed. Confirm the file remains ignored before deploying.');
